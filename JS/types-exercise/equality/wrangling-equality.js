const findAll = (val, arr) => {
    const results = [];
    for (let arrVal of arr) {
        if (Object.is(arrVal, val)) {
            results.push(arrVal);
        } else if ((val == undefined || val == null) && val == arrVal) {
            results.push(arrVal);
        } else if (typeof arrVal == "boolean") {
            if (arrVal === val) {
                results.push(val);
            }
        } else if (typeof arrVal == "string" && arrVal.trim() !== "" && typeof val == "number" && !Object.is(-0, val)) {
            if (arrVal == val) {
                results.push(arrVal);
            }
        } else if (typeof arrVal == "number" && !Object.is(arrVal,-0) && !Object.is(arrVal,NaN) && !Object.is(arrVal,Infinity) && !Object.is(arrVal,-Infinity) && typeof val == "string" && val.trim() != "") {
            if (arrVal == val) {
              results.push(arrVal);
            }
          }
    }

    return results;

}


